#include <iostream>
using namespace std;

int main()
{
	float number = 5.46;
	cout << "Float number is: " << number << endl;
	// Typecasting changes variable type but doesn't permanently converts variable into other data type
	cout << "Integer number is: " << int(number) << endl;
	cout << "Float number is: " << number << endl;

	return 0;
}
